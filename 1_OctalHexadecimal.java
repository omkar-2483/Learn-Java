// java supports octal and hexadecimal integers
// octal numbers have prefix 0
// hexadecimal have prefox 0X

class OctalHexadecimal{
    public static void main(String[] args){
        int num=010-02;
        System.out.println(num);
        int num2=0X10-0XF;
        System.out.print(num2);
    }
}